@Composable
fun VoidText() {
    Markdown(
        """
######   **空亡**：  
1. 时家奇门，到底是看“时空亡”还是“日空亡”？在学术论证方面一直意见不同。  
2. 旬空在奇门局中非常重要。古代的奇门旬空称为“孤”，对宫叫作“虚”。黄石公有言：“背孤击虚，一女胜十男。”虚空孤虚之内，必有玄机。  
3. 用神落空亡，肯定有事情要发生或已经发生。  
   - 日干落空亡之地：  
     1. 表示当事人目前心中没底、不知所措、心虚、缺乏自信。  
     2. 表示其状态不佳，存在缺陷，但多数情况下是“吉事不吉、凶事不凶”，类似一种无力之态。  
4. 时干临空亡宫，常主办事不成，因为时干主事体，代表所求之事的状态。  
5. 当然，并非时干逢空就一定办不成事。还需分旺衰、辨真空假空等综合判定。  

- 病星临空亡宫，主病情虚假不实，一般对冲之宫为玄机所在。  
- 测命盘落空亡，吉者减昌，苦者更苦，逢冲空填空之时吉凶必现。  
- 测婚姻，乙、庚或六合落空亡，表示尚无婚姻或一方未在家。  
- 丁与庚相生、丙与乙相生的情况下，若丙或丁落宫逢空亡，旺相则有情人，衰弱则无。  
- 日干逢空亡，吉事不吉、凶事不凶。  
- 时干逢空亡，表示事情难成。  
- 年命逢空亡，主不能白头偕老。  
- 值使门逢空亡，主事之人不作为。  

空亡既表示事情的时空点尚未到来，也可能代表是过去之事，或预示此事将来落空、空忙一场。在实际应用中须灵活掌握、区别对待。有时测老人情况遇空亡，常含人已不在的信息。  

事物临空亡，说明隐藏问题与隐患，是当前必须面对和解决之事。例如测工作，开门遇空亡，说明正面临工作方面的问题，且问题已暴露。若用神不空，但局中存在空亡之宫，则表示隐患，但此隐患不一定立即需面对，尚未影响事态发展。  

用神落空亡为直接影响；用神不空而它宫空亡为间接影响。  

空亡有真空与假空之分：  
- 用神在长生、冠带、临官、帝旺之地，或落旺相之宫，均为假空。太岁空不为真空。  
- 用神临死、墓、绝之地，则为真空。  
- 假空指天时未到、时机未成熟，时机一到仍可成事；真空则一事无成，纵填实、冲实亦难应验。  

实践中还须注意：  
- 空亡宫临马星或被马星冲，一般不为真空。  
- 日、时干、用神落衰死之宫又受旺宫所克，可视为真空。  
- 空亡之地若遇年月日时支相冲，则不为真空（需视具体应期而定）。  
- 就落宫而言，真空如：春土、夏金、秋木、冬火、四季水，此时无论填或冲皆无效。  

空亡即“没有”，故无所谓生克、旺衰、吉凶，因此空亡时“吉事不吉、凶事不凶”。假空逢冲实或填实，生克吉凶皆应验；真空则无论填冲均无效。有易友疑惑：若用神本就休囚无力、格局又凶，为真空，逢冲填是否会应大凶？实则不然，真空则凶事亦凶不起来，凶事空亡反为吉利。  

么学声老师对空亡研究透彻，提出“逢虚看孤、逢孤看虚”：  
- 用神宫逢孤（空亡），信息折射到对冲之宫的虚地；  
- 若用神宫不空，但对宫空亡，则该空亡宫亦是其信息折射。  
- 需注意：信息虽在对宫有反映，但事情结局与落脚点仍在原宫，而非移向对宫。对宫代表此事目前的停留状态，结局仍须回归本宫判断，应期常在对冲宫所对应的年月日时。  

判断空亡时，若需分主客，应辨明是主空亡还是客空亡：  
- 客空亡：有天气无地气，即有名无实、有机遇无位置、有职无权、有想法无行动等。  
- 主空亡：有地气无天气，即有实不显、有位置无机会、有行动无目标等。  

实践中观察：  
- 用神落宫反映当前状态；其先天宫位（如落巽宫空亡，巽宫先天为兑，则看后天兑宫）反映其溯源。用神宫空亡，意味着事物尚未成型，深挖其先天宫位信息是有必要的。即便不空亡，也可通过先天宫位寻找溯源信息。  
- 对于已存在且不空亡的实体宫，其后天位可一定程度上预示事物演变规律（如用神在巽宫，后续发展可参考坤宫，因后天坤宫为先天巽宫）。  
- 需注意，这些仅为参考信息。事物成败判断的根本仍在于本宫信息及相关用神之间的生克作用。先天、后天、逢孤看虚等方法，旨在获取更多信息以分析来龙去脉，并非判断的主体依据。
""".trimIndent()
    )
}